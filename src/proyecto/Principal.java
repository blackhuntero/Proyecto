    private void GrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GrabarActionPerformed
        // TODO add your handling code here:
        Archivos conexion = new Archivos ();
        Atributos dato1 = new Atributos();
        
        dato1.setNombre(Nombre.getText().toString());
        dato1.setMatricula(Matricula.getText().toString());
        dato1.setCorreo(Correo.getText().toString());
        dato1.setPromedio(Promedio.getText().toString());
        
        conexion.Agregar(dato1);
        Datos1 = conexion.getDatos1();
        
        if(conexion.Grabar(Datos1, 0)){
            tabla = (DefaultTableModel) Datos.getModel();
            tabla.getDataVector();
            JOptionPane.showMessageDialog(this, "Se grabo correctamente",
                    "Grabado", JOptionPane.INFORMATION_MESSAGE);
            CargaRegistrosTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Error al grabar",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_GrabarActionPerformed

    private void PromediarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PromediarActionPerformed
        // TODO add your handling code here:
        double result;
        double num;
        double cantidad=0;
        
        num = Integer.parseInt(showInputDialog(this, "Â¿Cuantas calif se van a promediar?"));
        
        for(int i =0; i<=num; i++){
        double cal = Integer.parseInt(showInputDialog(this, "Ingrese la calificacion"));
         cantidad = cantidad + cal;
        }
        result = cantidad/num;
        String Pro = String.valueOf(result);
        Promedio.setText(Pro);
        JOptionPane.showMessageDialog(null,"Se ha grabado el promedio");
        
        
    }//GEN-LAST:event_PromediarActionPerformed

    private void DatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DatosMouseClicked
        // TODO add your handling code here:
         int filas = Datos.getSelectedRowCount();
        int fila = 0;
        if(filas == 1 ){
            fila = Datos.getSelectedRow();
            posicion = fila;
            Nombre.setText(Datos.getValueAt(fila, 0).toString());
            Matricula.setText(Datos.getValueAt(fila, 1).toString());
            Correo.setText(Datos.getValueAt(fila, 2).toString());
            Promedio.setText(Datos.getValueAt(fila, 3).toString());
        }
    }//GEN-LAST:event_DatosMouseClicked

    private void ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarActionPerformed
        // TODO add your handling code here:
        
        Archivos conexion = new Archivos();
        Atributos dato1 = new Atributos();
        
        dato1.setNombre(Nombre.getText().toString());
        dato1.setCorreo(Correo.getText().toString());
        dato1.setMatricula(Matricula.getText().toString());
        dato1.setPromedio(Promedio.getText().toString());
        
        Datos1.set(posicion, dato1);
        
        if(conexion.Grabar(Datos1,1)){
            tabla =(DefaultTableModel) Datos.getModel();
            tabla.getDataVector().removeAllElements();
            JOptionPane.showMessageDialog(this, "Se actualizo correctamente", 
                    "Actualizado", JOptionPane.INFORMATION_MESSAGE);
            Cargar();
        } else{
            JOptionPane.showMessageDialog(this, "Error al Actualizar",
                    "Error", JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_ActualizarActionPerformed

